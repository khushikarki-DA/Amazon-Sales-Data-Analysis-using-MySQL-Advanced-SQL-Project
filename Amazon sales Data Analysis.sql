9.Inventory Restock Optimization
Find products likely to go out of stock soon based on current sales velocity.
Challenge: Include the quantity sold by each product and its stock remaining.
*/

WITH product_sales AS
(
    SELECT
        p.product_id,
        p.product_name,
        SUM(oi.quantity) AS total_quantity_sold
    FROM products p
    JOIN order_items oi
        ON p.product_id = oi.product_id
    GROUP BY p.product_id, p.product_name
)

SELECT
    p.product_name,
    i.stock,
    ps.total_quantity_sold,
    ROUND(i.stock / ps.total_quantity_sold,2) AS stock_ratio
FROM inventory i
JOIN products p
    ON i.product_id = p.product_id
JOIN product_sales ps
    ON p.product_id = ps.product_id
ORDER BY stock_ratio ASC;

