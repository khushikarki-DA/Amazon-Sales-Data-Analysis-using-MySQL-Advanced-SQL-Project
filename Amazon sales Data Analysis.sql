4. Monthly Sales Trend
Query monthly total sales over the past year.
Challenge: Display the sales trend, grouping by month, return current_month sale, last month sale!
*/

SELECT year_, month_, total_sale as current_month_sale,
LAG(total_sale, 1) OVER(ORDER BY year_, month_) as last_month_sale
FROM(
SELECT MONTH(o.order_date) AS Month_,YEAR(O.ORDER_DATE) AS Year_, SUM(oi.total_price) AS total_sale
FROM orders o
INNER JOIN order_items as oi
ON o.order_id = oi.order_id 
WHERE o.order_date>= current_date()- interval 1 year
GROUP BY MONTH(o.order_date),YEAR(O.ORDER_DATE)
ORDER BY Year_, Month_
)T;

