GROUP BY c.customer_id, Full_name, o.order_date 
),
lag_data AS
(
    SELECT *,
           LAG(order_value,1) OVER(PARTITION BY customer_id ORDER BY order_date) AS prev1,
           LAG(order_value,2) OVER(PARTITION BY customer_id ORDER BY order_date) AS prev2
    FROM customer_orders
)

SELECT *
FROM lag_data
WHERE order_value > prev1
AND prev1 > prev2;

