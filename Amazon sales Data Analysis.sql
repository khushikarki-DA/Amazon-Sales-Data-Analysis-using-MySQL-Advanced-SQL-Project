/*
20. Month-over-Month Sales Growth
Calculate monthly revenue growth percentage 
Challenge: Include revenue, previous month sales.
*/

WITH monthly_sales AS
(
    SELECT
        DATE_FORMAT(o.order_date,'%Y-%m') AS month_,
        SUM(oi.total_price) AS revenue
    FROM orders o
    JOIN order_items oi
        ON o.order_id = oi.order_id
    GROUP BY month_
)

SELECT
    month_,
    revenue,
    LAG(revenue) OVER(ORDER BY month_) AS previous_month_sales,
    ROUND(
        ((revenue - LAG(revenue) OVER(ORDER BY month_)) /
        LAG(revenue) OVER(ORDER BY month_)) * 100,2
    ) AS growth_percentage
FROM monthly_sales;

